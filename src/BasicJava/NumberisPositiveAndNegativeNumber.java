package BasicJava;

public class NumberisPositiveAndNegativeNumber {

	public static void main(String[] args) {

		double Num = 0;

		if (Num > 0) {
			System.out.println("The Number is a Positive Number: " + Num);
		} else if (Num < 0) {
			System.out.println("The Number is a Negative Number: " + Num);
		} else {
			System.out.println("The Number is Zero: " + Num);
		}
	}

}
